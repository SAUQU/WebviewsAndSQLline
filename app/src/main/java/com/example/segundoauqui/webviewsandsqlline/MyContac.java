public class MyContac {



    private String name;
    private String number;

    public void setName(String name) {
        this.name = name;
    }

    public MyContac(String name, String number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }






}
